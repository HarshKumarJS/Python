# Program to count the number of students with grade "A" in a tuple

# Tuple containing student grades
t = ("C", "D", "A", "A", "B", "B", "A")

# Count the number of times grade 'A' appears in the tuple
count_a = t.count('A')

# Print the result
print(f"The total number of students with grade A is/are {count_a}")