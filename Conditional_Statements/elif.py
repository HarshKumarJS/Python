# Program to check if a number is positive, negative, or zero

# Assign a number to check
num = -5

# Use if-elif-else to classify the number
if num > 0:
    print("The number is positive.")   # Greater than 0 → Positive
elif num < 0:
    print("The number is negative.")   # Less than 0 → Negative
else:
    print("The number is zero.")       # Equal to 0 → Zero
