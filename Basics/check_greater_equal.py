# Program to check if 'a' is greater than or equal to 'b'

# Taking integer input for 'a' and 'b'
a = int(input("Enter a: "))
b = int(input("Enter b: "))

# Checking the condition: if a is greater than or equal to b
if a >= b:
    print("True")   # Prints True if condition is satisfied
else:
    print("False")  # Prints False if condition is not satisfied
